<h1 class="text-center">Chess - Game of <strike>Kings</strike> Nerds</h1>
<h2><span>♔</span> King</h2>
<p>He can move in any direction but only one field per round.</p>
<h2><span>♕</span> Queen</h2>
<p>She can be moved like Bishop and Rook.</p>
<h2><span>♖</span> Rook</h2>
<p>He can be moved forward and sidewards until he reached another figure.</p>
<h2><span>♗</span> Bishop</h2>
<p>He can be moved diagonal in any direction until he reached another figure.</p>
<h2><span>♘</span> Knight</h2>
<p>He can be moved one field straight and one field diagonal in any order.</p>
<h2><span>♙</span> Pawn</h2>
<p>He can be moved only forwards. On the first turn for each pawn he can be moved two fields on following turns only one field. Other figures can only be conquered diagonal.</p>